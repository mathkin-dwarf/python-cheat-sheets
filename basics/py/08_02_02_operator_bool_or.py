# 論理演算子(or)

print('num = 5')
num = 5
if num >= 0 or num % 2 == 1:   # 0以上もしくは奇数
    print('  num is "positive or odd"')
else:
    print('  num is not "positive or odd"')

print('num = 10')
num = 10
if num >= 0 or num % 2 == 1:   # 0以上もしくは奇数
    print('  num is "positive or odd"')
else:
    print('  num is not "positive or odd"')

print('num = -10')
num = -10
if num >= 0 or num % 2 == 1:   # 0以上もしくは奇数
    print('  num is "positive or odd"')
else:
    print('  num is not "positive or odd"')
